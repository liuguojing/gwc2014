				Most citizens will require a VISA or ETA for entry into Sydney. 
			  <ul><li>If you are deviating from your direct route to Winners Circle in Sydney you may also need to obtain a visa for additional places you are visiting. </li> 
<li>Some Winners will be required to obtain a transit visas if flying indirectly to Australia, please check with your local embassy</li></ul>
  For international travelers entering Australia to gain entry or transit through to Sydney or with a deviation you must apply online in advance of travel for your visa, using the following links listed below:
  <ul><li><strong>eVisitor (see information below)</strong></li>
  <li><b>ETA (see information below)</b></li>
  <li><b>Visitor (Subclass 600) Visa (See information below)</b></li>
  <li><b>Non eligible</b> citizens of countries that do NOT participate in the e.Visitor, ETA, Visitor (Subclass 600) Visa program will require a visa to enter Australia.  Please contact your local embassy in order to arrange this. The registration team will be able to provide you with an invitation letter for this purpose.</li>
